import 'dart:convert';
import 'dart:io';

import 'package:hostr_cli/src/actions/hostr_actions.dart';
import 'package:path/path.dart' as p;

void main(List<String> args) {
  final repoRoot = args.isNotEmpty
      ? Directory(args.first)
      : Directory(p.normalize(p.join(Directory.current.path, '..')));
  final target = File(
    p.join(
      repoRoot.path,
      'ai',
      'mcp-server',
      'src',
      'generated',
      'hostr-actions.ts',
    ),
  );
  target.parent.createSync(recursive: true);
  target.writeAsStringSync(generateHostrActionsTypescript());
  stdout.writeln('Generated ${target.path}');
}

String generateHostrActionsTypescript() {
  final buffer = StringBuffer()
    ..writeln('// Generated by hostr_cli/bin/generate_mcp_types.dart.')
    ..writeln(
      '// Source of truth: hostr_cli/lib/src/actions/hostr_actions.dart.',
    )
    ..writeln('import { z } from "zod";')
    ..writeln();

  final interfaces = <String>{};
  for (final spec in HostrActionCatalog.all) {
    interfaces.add(spec.typescriptInput.trim());
  }
  buffer
    ..writeln(interfaces.join('\n\n'))
    ..writeln();

  final schemasByInputType = <String, Map<String, Object?>>{};
  for (final spec in HostrActionCatalog.all) {
    final previous = schemasByInputType[spec.inputTypeName];
    if (previous != null &&
        jsonEncode(previous) != jsonEncode(spec.inputSchema)) {
      throw StateError(
        'Conflicting schemas for ${spec.inputTypeName}. '
        'Use distinct input type names or share the same schema.',
      );
    }
    schemasByInputType[spec.inputTypeName] = spec.inputSchema;
  }

  for (final entry in schemasByInputType.entries) {
    buffer
      ..writeln(
        'export const ${entry.key}Schema = ${_zodForSchema(entry.value)};',
      )
      ..writeln();
  }

  buffer
    ..writeln(
      'export const hostrActionDocumentation = ${jsonEncode(HostrActionCatalog.documentationMarkdown())};',
    )
    ..writeln()
    ..writeln('export const hostrActionCatalog = [');
  for (final spec in HostrActionCatalog.all) {
    buffer
      ..writeln('  {')
      ..writeln('    id: ${jsonEncode(spec.id)},')
      ..writeln('    toolName: ${jsonEncode(spec.mcpToolName)},')
      ..writeln('    title: ${jsonEncode(spec.title)},')
      ..writeln('    description: ${jsonEncode(spec.mcpDescription)},')
      ..writeln('    inputTypeName: ${jsonEncode(spec.inputTypeName)},')
      ..writeln('    inputJsonSchema: ${_jsonLiteral(spec.inputSchema)},')
      ..writeln('    inputSchema: ${spec.inputTypeName}Schema,')
      ..writeln('    readOnly: ${spec.readOnly},');
    if (spec.requiredRole != null) {
      buffer.writeln('    requiredRole: ${jsonEncode(spec.requiredRole)},');
    }
    buffer.writeln('  },');
  }
  buffer
    ..writeln('] as const;')
    ..writeln()
    ..writeln(
      'export type HostrActionId = (typeof hostrActionCatalog)[number]["id"];',
    )
    ..writeln()
    ..writeln(
      'export const hostrActionByToolName = new Map(hostrActionCatalog.map((action) => [action.toolName, action]));',
    )
    ..writeln()
    ..writeln(
      'export const hostrActionById = new Map(hostrActionCatalog.map((action) => [action.id, action]));',
    );

  return buffer.toString();
}

String _zodForSchema(Map<String, Object?> schema) {
  final type = schema['type'];
  final description = schema['description'] as String?;
  final format = schema['format'] as String?;
  final defaultValue = schema['default'];
  String expr;

  switch (type) {
    case 'object':
      final properties =
          (schema['properties'] as Map?)?.cast<String, Object?>() ?? const {};
      if (properties.isEmpty && schema['additionalProperties'] != false) {
        expr = 'z.record(z.string(), z.unknown())';
        break;
      }
      final requiredFields =
          (schema['required'] as List?)
              ?.map((item) => item.toString())
              .toSet() ??
          const <String>{};
      final entries = properties.entries
          .map((entry) {
            final child = Map<String, Object?>.from(entry.value as Map);
            var value = _zodForSchema(child);
            if (!requiredFields.contains(entry.key)) {
              value = '$value.optional()';
            }
            return '  ${jsonEncode(entry.key)}: $value';
          })
          .join(',\n');
      expr = 'z.object({\n$entries\n})';
      if (schema['additionalProperties'] == false) expr = '$expr.strict()';
      break;
    case 'string':
      expr = 'z.string()';
      if (format == 'date-time') expr = '$expr.datetime()';
      break;
    case 'integer':
      expr = 'z.number().int()';
      if (schema['minimum'] != null) expr = '$expr.min(${schema['minimum']})';
      if (schema['maximum'] != null) expr = '$expr.max(${schema['maximum']})';
      break;
    case 'number':
      expr = 'z.number()';
      if (schema['minimum'] != null) expr = '$expr.min(${schema['minimum']})';
      if (schema['maximum'] != null) expr = '$expr.max(${schema['maximum']})';
      break;
    case 'boolean':
      expr = 'z.boolean()';
      break;
    case 'array':
      expr =
          'z.array(${_zodForSchema(Map<String, Object?>.from(schema['items'] as Map))})';
      if (schema['minItems'] != null) expr = '$expr.min(${schema['minItems']})';
      break;
    default:
      expr = 'z.unknown()';
  }

  if (description != null) {
    expr = '$expr.describe(${jsonEncode(description)})';
  }
  if (defaultValue != null) {
    expr = '$expr.default(${jsonEncode(defaultValue)})';
  }
  return expr;
}

String _jsonLiteral(Object? value) {
  const encoder = JsonEncoder.withIndent('    ');
  return encoder.convert(value);
}
